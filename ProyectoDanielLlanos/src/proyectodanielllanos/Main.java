private void regVehiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_regVehiculoActionPerformed
    if (validarCamposLlenosVehiculo()) {
        Propietario p = new Propietario(propNombre.getText(), Integer.parseInt(propId.getText()), Integer.parseInt(PropTel.getText()));
        Referencia r = null;
        for (int i = 0; i < referencias.size(); i++) {
            if ((selRef.getSelectedItem() + "").equals(referencias.get(i).getNombre())) {
                r = referencias.get(i);
            }
        }
        if (afiliado) {
            if (nroAfiliacion.getText().equalsIgnoreCase("")) {
                JOptionPane.showMessageDialog(this, "Digite el numero de afiliacion");
            } else {
                Afiliado a = new Afiliado(Integer.parseInt(nroAfiliacion.getText()), Integer.parseInt(modelo.getText()), placa.getText(), p, r, new ArrayList<Revision>());
                vehiculos.add(a);
                JOptionPane.showMessageDialog(propNombre, "Registro Exitoso");
            }
        } else {
            if (soat.getText().equalsIgnoreCase("")) {
                JOptionPane.showMessageDialog(this, "Digite el numero de SOAT");
            } else {
                Ocasional o = new Ocasional(aseguradora.getSelectedItem() + "", Integer.parseInt(soat.getText()), Integer.parseInt(modelo.getText()), placa.getText(), p, r, new ArrayList<Revision>());
                vehiculos.add(o);
                JOptionPane.showMessageDialog(propNombre, "Registro Exitoso");
            }
        }

        limpiarPanelRegVehiculo();

    }
}//GEN-LAST:event_regVehiculoActionPerformed

