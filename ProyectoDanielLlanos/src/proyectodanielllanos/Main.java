    private void agregarRevision(String placa, Revision r) {
        for (int i = 0; i < vehiculos.size(); i++) {
            if (placa.equals(vehiculos.get(i).getPlaca())) {
                vehiculos.get(i).agregarRevision(r);
            }
        }

    }

    private boolean validarCamposLlenosReferencia() {
        if (nombreRef.getText().equalsIgnoreCase("") || nroPuertas.getText().equalsIgnoreCase("") || cilindraje.getText().equalsIgnoreCase("") || velMax.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Debe rellenar todos los campos");
            return false;
        } else {
            return true;
        }
    }

    private boolean validarCamposLlenosVehiculo() {
        if (placa.getText().equalsIgnoreCase("") || modelo.getText().equalsIgnoreCase("") || propId.getText().equalsIgnoreCase("") || PropTel.getText().equalsIgnoreCase("") || propNombre.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Debe rellenar todos los campos");
            return false;
        } else {
            return true;
        }
    }

    private boolean validarCamposLlenosRegRevisiones() {
        if (busPlaca.getText().equalsIgnoreCase("") || fecha.getText().equalsIgnoreCase("") || descripcion.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Debe rellenar todos los campos");
            return false;
        } else {
            return true;
        }
    }

    private Vehiculo retornarVehiculo(String placa) {
        for (int i = 0; i < vehiculos.size(); i++) {
            if (placa.equals(vehiculos.get(i).getPlaca())) {
                return vehiculos.get(i);
            }

        }
        JOptionPane.showMessageDialog(this, "El vehiculo con esa placa no existe");
        return null;
    }

    private String buscarPorGama(String gama) {
        String msj = "";
        for (int i = 0; i < vehiculos.size(); i++) {
            if (gama.equalsIgnoreCase(vehiculos.get(i).getReferencia().getGama())) {
                if (vehiculos.get(i) instanceof Afiliado) {
                    Afiliado a = (Afiliado) vehiculos.get(i);
                    msj += a.getInfo() + "\n";
                    ;
                } else {
                    Ocasional o = (Ocasional) vehiculos.get(i);
                    msj += o.getInfo() + "\n";
                    ;
                }
            }
            if (msj.length() == 0) {
                msj = "No hay vehiculos registrados con esa gama";
            }
        }
        return msj;
    }

    private void limpiarPanelRevision() {
        placa.setText("");
        fecha.setText("");
        descripcion.setText("");
        nroRevision.setText("");
    }
