    private Trabajador buscarTrabajador(String id) {
        for (int i = 0; i < trabajadoresQuema.length; i++) {
            if (trabajadoresQuema[i] != null) {
                if (trabajadoresQuema[i].getId().equals(id)) {
                    return trabajadoresQuema[i];
                }
            }
        }
        for (int i = 0; i < trabajadoresSinQuema.length; i++) {
            if (trabajadoresSinQuema[i] != null) {
                if (trabajadoresSinQuema[i].getId().equals(id)) {
                    return trabajadoresSinQuema[i];
                }
            }
        }
        return null;
    }

    private void actualizarReportes() {
        String quema = "";
        String sinQuema = "";

        for (int i = 0; i < trabajadoresQuema.length; i++) {
            if (trabajadoresQuema[i] != null) {
                quema += trabajadoresQuema[i] + "\n";
            }
        }
        for (int i = 0; i < trabajadoresSinQuema.length; i++) {
            if (trabajadoresSinQuema[i] != null) {
                sinQuema += trabajadoresSinQuema[i] + "\n";
            }
        }
        tConQuema.setText(quema);
        tSinQuema.setText(sinQuema);

        String meses[] = new String[12];

    }

    private void actualizarComboCedulas() {
        String[] ids = new String[12];
        for (int i = 0; i < trabajadoresQuema.length; i++) {
            ids[i] = trabajadoresQuema[i].getId();
            ids[i + 6] = trabajadoresSinQuema[i].getId();
        }
        tIds.setModel(new DefaultComboBoxModel(ids));
    }
