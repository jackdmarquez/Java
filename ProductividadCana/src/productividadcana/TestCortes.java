    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (buscarTrabajador(tIdR.getText()) != null) {
            JOptionPane.showMessageDialog(null, "Esta identificacion ya existe");
        } else {
            String nombre = tNombreR.getText();
            String id = tIdR.getText();
            Trabajador t = new Trabajador(new Corte[30], nombre, id, tTipo.getSelectedItem() + "");
            if ((tTipo.getSelectedItem() + "").equals("Quema")) {
                if (contadorQuema < 6) {
                    trabajadoresQuema[contadorQuema] = t;
                    contadorQuema++;
                } else {
                    JOptionPane.showMessageDialog(null, "La cantidad maxima de trabajadores para este tipo fue registrada.");
                    fQuema = true;
                }
            } else {
                if (contadorSinQuema < 6) {
                    trabajadoresSinQuema[contadorSinQuema] = t;
                    contadorSinQuema++;
                } else {
                    JOptionPane.showMessageDialog(null, "La cantidad maxima de trabajadores para este tipo fue registrada.");
                    fSinQuema = true;
                }
            }
            if (contadorQuema == 6 && contadorSinQuema == 6) {
                jTabbedPane1.setEnabledAt(1, true);
                jTabbedPane1.setEnabledAt(2, true);
                jTabbedPane1.setEnabledAt(0, false);
                jTabbedPane1.setSelectedIndex(1);
                actualizarComboCedulas();
            }
            contadores.setText("Cortadores con quema: " + (contadorQuema) + " Contadores Sin Quema: " + (contadorSinQuema));

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String id = tIds.getSelectedItem() + "";
        double horasT = Double.parseDouble(tHorasT.getText());
        double hectareas = Double.parseDouble(tHectareasC.getText());
        String dia = tDia.getSelectedItem() + "";

        Trabajador buscado = buscarTrabajador(id);

        if (buscado != null) {
            Corte c = new Corte(horasT, hectareas, dia, buscado.getTipo());
            if (buscado.agregarCorte(c, tDia.getSelectedIndex())) {
                JOptionPane.showMessageDialog(null, "Corte registrado");
                a.escribirSala(trabajadoresQuema, trabajadoresSinQuema);
                actualizarReportes();
            } else {
                JOptionPane.showMessageDialog(null, "Este trabajador ya tiene un corte registrado este dia");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Identificacion no encontrada");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

